# List Operations
lst = [5, 2, 9, 1]
print("Original List:", lst)
lst.append(7)
print("After append 7:", lst)
lst.insert(2, 15)
print("After insert 15 at index 2:", lst)
lst.remove(2)
print("After removing 2:", lst)
lst.sort()
print("Sorted List:", lst)
lst.reverse()
print("Reversed List:", lst)
print("Sliced List (1:4):", lst[1:4])
